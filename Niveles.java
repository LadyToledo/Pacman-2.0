	public void initLevel() { //nivel de inicio
        int i;
        if (score ==0) {
	        for (i = 0; i < nrofblocks * nrofblocks; i++) {
	            screendata[i] = leveldata[i];//datos pantalla, datos nivel
	        }
        }else {
        	int a;
        	for (a = 0; a < nrofblocks * nrofblocks; a++) {
                screendata[a] = leveldata2[a];//datos pantalla, datos nivel
            }
        }
        continueLevel();
        nrofghosts = 6;
    }

    public void continueLevel() { //continuar nivel
        short i;
        int dx = 1;
        int random;
        for (i = 0; i < nrofghosts; i++) {
            ghosty[i] = 4 * blocksize;
            ghostx[i] = 4 * blocksize;
            ghostdy[i] = 0;
            ghostdx[i] = dx;
            dx = -dx;
            random = (int) (Math.random() * (currentspeed + 1));
            if (random > currentspeed) {
                random = currentspeed;
            }
            ghostspeed[i] = validspeeds[random];
        }
        pacmanx = 7 * blocksize;
        pacmany = 11 * blocksize;
        pacmandx = 0;
        pacmandy = 0;
        reqdx = 0;
        reqdy = 0;
        viewdx = -1;
        viewdy = 0;
        dying = false;
    }
